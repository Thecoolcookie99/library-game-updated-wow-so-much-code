function spellItem () {
    spell1 = ""
    spell2 = ""
    spell3 = ""
    spell4 = ""
    spell5 = ""
    finished = 0
    speakItem = 1
    letter = 0
    for (let index = 0; index < 49; index++) {
        if (letter == 9) {
            letter = -1
            speakItem += 1
        } else {
            if (speakItem == 1) {
                spell1 = "" + spell1 + list[speakItem - 1].charAt(letter)
            } else if (speakItem == 2) {
                spell2 = "" + spell2 + list[speakItem - 1].charAt(letter)
            } else if (speakItem == 3) {
                spell3 = "" + spell3 + list[speakItem - 1].charAt(letter)
            } else if (speakItem == 4) {
                spell4 = "" + spell4 + list[speakItem - 1].charAt(letter)
            } else {
                spell5 = "" + spell5 + list[speakItem - 1].charAt(letter)
            }
            if (!(list[speakItem - 1].charAt(letter) == " " || list[speakItem - 1].charAt(letter) == "")) {
                music.play(music.createSoundEffect(WaveShape.Sine, 224, 16, 255, 255, 100, SoundExpressionEffect.None, InterpolationCurve.Linear), music.PlaybackMode.InBackground)
            }
            sprites.destroy(line1)
            sprites.destroy(line2)
            sprites.destroy(line3)
            sprites.destroy(line4)
            sprites.destroy(line5)
            line1 = textsprite.create(spell1, 0, 15)
            line1.x = 126
            line1.y = 7
            line2 = textsprite.create(spell2, 0, 15)
            line2.x = 126
            line2.y = 14
            line3 = textsprite.create(spell3, 0, 15)
            line3.x = 126
            line3.y = 21
            line4 = textsprite.create(spell4, 0, 15)
            line4.x = 126
            line4.y = 28
            line5 = textsprite.create(spell5, 0, 15)
            line5.x = 126
            line5.y = 35
            if (!(controller.B.isPressed()) || letter > messagee) {
                pause(50)
            }
        }
        letter += 1
    }
}
